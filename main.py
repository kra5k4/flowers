@dp.message_handler(commands=['start'])
async def start_cmd(message: types.Message):
    await message.answer("🌸 Привет! Пришли мне фото цветка, и я определю его вид.")


@dp.message_handler(content_types=['photo'])
async def handle_photo(message: types.Message):
    msg = await message.answer("🔍 Изучаю фото...")

    try:
        # Скачиваем фото
        photo_io = io.BytesIO()
        await message.photo[-1].download(destination_file=photo_io)
        photo_io.seek(0)

        # Подготовка изображения
        img = Image.open(photo_io).convert('RGB')
        img = img.resize((IMG_SIZE, IMG_SIZE))

        # Преобразование в массив и нормализация
        img_array = np.array(img).astype('float32')
        img_array = (img_array / 127.5) - 1.0
        img_array = np.expand_dims(img_array, axis=0)

        # Предсказание
        preds = model.predict(img_array)
        idx = np.argmax(preds[0])
        conf = np.max(preds[0]) * 100

        flower = LABELS_RU[idx]

        await msg.delete()
        await message.reply(
            f"🌿 Это **{flower.upper()}**\n"
            f"📊 Точность: {conf:.1f}%",
            parse_mode="Markdown"
        )

    except Exception as e:
        logging.error(e)
        await msg.edit_text("😢 Не удалось распознать. Попробуй другое фото.")


